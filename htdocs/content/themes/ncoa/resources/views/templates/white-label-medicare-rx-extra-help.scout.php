      <h2 class="page-header">The Application Process</h2>

      <p>Medicare has a program that helps pay for your prescription medicine. It's called Medicare Prescription Drug Coverage or Medicare Part D. This program gives you a choice of prescription plans that offer various types of coverage. If you have or can enroll in Medicare Part D and you have limited income and resources, you may be able to get "Extra Help" with your prescription drug costs. This benefit is known as Medicare Part D Low-Income Subsidy (LIS) or Extra Help. Depending on your income and resources, you will have low or no deductible, low or no premiums, no coverage gap (also called the "doughnut hole"), and will pay much less for your prescriptions. Extra Help can provide you an average value of $4,000 per year. However, to get Extra Help, you must be enrolled in a Medicare Prescription Drug plan.

	If you have Medicare and have limited income and resources, you may be able to get Extra Help paying for your prescription drug costs if:
	<ul>
 	<li>Your income is less than $18,090 if single and $24,360 if married. If you live in Alaska or Hawaii, you may still get help even if your income is higher than these limits.</li>
 	<li>You have resources less than $13,820 if single and $27,600 if married.</li>
	</ul>
	</p>

      <div class="accordian-wrap">
        <p class="accordian-trigger"><strong>Who Should Apply for Extra Help?</strong></p>

        <div class="accordian-content" style="display: none;">
          <p>You should complete this online application for Extra Help if:
	<ul>
 	<li>You have Medicare Part A (Hospital Insurance) and/or Medicare Part B (Supplementary Medical Insurance); and</li>
 	<li>You live in one of the 50 states or the District of Columbia; and</li>
 	<li>Your income is below $18,090 if you are not currently married or not living with your spouse, or $24,360 if you are currently married and living with your spouse; and</li>
 	<li>Your combined savings, investments, and real estate are not worth more than $13,820 if you are not currently married or not living with your spouse, or $27,600 if you are currently married and living with your spouse. <strong> (DO NOT include the home you live in, vehicles, personal possessions, life insurance, burial plots, irrevocable burial contracts or back payments from Social Security or SSI.) </strong></li>
	</ul>
	If you have more than those amounts, you may not qualify for the Extra Help. However, you can still enroll in an approved Medicare prescription drug plan for coverage.
	</p>
        </div>
      </div>

      <div class="accordian-wrap">
        <p class="accordian-trigger"><strong>Who Should Not Apply for Extra Help?</strong></p>

        <div class="accordian-content" style="display: none;">
          You should not complete this online application if:
	<ul>
 	<li>You receive Medicaid and have Medicare, or</li>
 	<li>Your State pays for your Medicare premiums (Medicare Savings Program), or</li>
 	<li>You receive Supplemental Security Income (SSI) payments and have Medicare.</li>
	</ul>
	<i><strong>If you are in one of the 3 groups above, you will automatically get the Extra Help and do not need to apply. </strong></i>        </div>
      </div>  


     <div class="accordian-wrap">
        <p class="accordian-trigger"><strong>Can I Get Help If I Don't Have a Medicare Prescription Drug Plan?</strong></p>

        <div class="accordian-content" style="display: none;">
          <p>If you apply for and get the Extra Help and you have not yet enrolled in a Medicare Prescription Drug plan (Medicare Part D), you may temporarily get prescription drug coverage through Medicare's Limited Income NET (LI NET) Program. This program was created by Centers for Medicaid and Medicare Services (CMS) to eliminate gaps in prescription drug coverage for low-income Medicare Beneficiaries. The LI NET program is temporary and can only help you for a short time (usually about 2 months). If after this time, you have not chosen a Medicare Part D plan, CMS will automatically enroll you into one. For more information about LI NET, go to: <a href="https://www.humana.com/pharmacy/pharmacists/linet" target="_blank" rel="noopener noreferrer">www.Humana.com/LINET</a> or call (800) 783-1307.
	</p>       
	</div>
      </div>  


	<div class="accordian-wrap">
        <p class="accordian-trigger"><strong>Can I Get Help With Other Medicare Costs?</strong></p>

        <div class="accordian-content" style="display: none;">
          <p>You may be able to get help from your State with other Medicare costs under the Medicare Savings Programs which can save you up to $134 per month. By completing this online application for Extra Help, you will start the application process for a Medicare Savings Program. SSA will send information from your Extra Help application to your State, unless you tell SSA not to when you complete this form. Someone from your State will contact you to help you complete your Medicare Savings Program application.
 
	</p>       
