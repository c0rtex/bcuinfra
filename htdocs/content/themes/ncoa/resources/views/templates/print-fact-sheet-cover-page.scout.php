<h1 style="text-align: center">Your BenefitsCheckUp Report</h1>
<p>BenefitsCheckUp has prepared the following report identifying benefit programs for you to consider. Many
  of these programs will help you save money on health care, prescriptions, utilities, food assistance, and
  other services. For your convenience, this report includes program details such as: program descriptions,
  application forms, local contact information that answers questions and helps with enrollment, and a listing
  of necessary documentation to aid in the application process.</p>
<p style="font-weight: bold">It is important to note that final program eligiblity determination can only be made by the agencies
  administering the program(s). We encourage you to contact the agencies with any any questions you
  may have.</p>