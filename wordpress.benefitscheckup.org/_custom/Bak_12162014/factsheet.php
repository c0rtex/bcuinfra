                        <div class="clearfix">
                            <label for="xlInput"><h4><i class="icon-question-sign "></i> How can this program help me?</h4>
                                <p>SafeLink Wireless&reg; gives you a free cell phone and minutes to use, mobile access to emergency services, and a choice of cell phone plans if you meet the program guidelines. There are no contracts, no hidden fees, and no monthly charges. The cell phone has many of the following services:
                                <ul>
                                    <li>voicemail</li>
                                    <li>text</li>
                                    <li>call waiting</li>
                                    <li>international calling</li>
                                    <li>callingID</li>
                                 </ul>
                                The cell phones and minutes are provided by TracFone Wireless. The phone will be mailed to your home address (you cannot have it sent to a P.O. Box).</label>
                        </div>
					
                        <div class="clearfix">
                            <label for="xlInput"><h4><i class="icon-user "></i> Can I apply?</h4>
                                <p>You can take part in this program if you have limited income or receive help from other benefit programs such as:
                                <ul>
                                    	<li>Medicaid</li>
    									<li>Supplemental Security Income (SSI)</li>
    									<li>Temporary Assistance for Needy Families (TANF)</li>
    									<li>Low Income Home Energy Assistance Program (LIHEAP)</li>
    									<li>Supplemental Nutrition Assistance Program (SNAP)</li>
                                 </ul>
                                <strong>Note:</strong> If you get Lifeline from another cell phone provider, you may not take part in the SafeLink Wireless&reg; program with TracFone.</p></label>
                        </div>
                        
                        <div class="clearfix">
                            <label for="xlInput"><h4><i class="icon-user "></i> How do I apply?</h4>
                                <p>You must fill out an application online at the SafeLink Wireless&reg; Web site.<p>
								<p><strong>Note</strong>: The SafeLink Wireless&reg; program may only be offered in certain cities/towns in your state. Enter your zip code to find out if the program is offered in your area.</p></label>
